package br.ufes.prova2.model;

/**
 *
 * @author 55289
 */
public class Imagem {

    public int id;
    public String path;

    public Imagem(int id, String path) {
        this.id = id;
        this.path = path;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

}
